#include "libftprintf.h"

int		is_ok(char c)
{
	if (is_flag(c) || c == '.' || (c >= '0' && c <= '9') || c == '%'
			|| c == 'h' || c == 'l' || c == 'L' || c == '*')
		return (1);
	return (0);
}
